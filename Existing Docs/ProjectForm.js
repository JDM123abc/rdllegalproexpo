  const calculateDuration = (index, stages) => {
    const stage = stages[index];
    if (!stage.startingDate || !stage.completionDate) return "TBD";
    const end = new Date(stage.completionDate);
    const start = index === 0 ? new Date(stage.startingDate) : new Date(stages[index - 1].completionDate || stage.startingDate);
    const diffMs = end - start;
    if (diffMs < 0) return "Invalid";
    const diffDays = Math.floor(diffMs / (24 * 3600 * 1000));
    const remainingMs = diffMs % (24 * 3600 * 1000);
    const remainingHours = Math.floor(remainingMs / (3600 * 1000));
    const remainingSeconds = Math.floor((remainingMs % (3600 * 1000)) / 1000);
    const totalWorkingHours = diffDays * 8 + remainingHours;
    return `${totalWorkingHours} Hrs. (${remainingSeconds} Sec)`;
  };

