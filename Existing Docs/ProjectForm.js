    const durationHrs = parseDurationToHours(stageDuration);
    const planned = parseFloat(plannedHrs) || 0;
    const diff = planned - durationHrs;
    return diff >= 0
      ? { text: `+${diff.toFixed(1)} Hrs Reserve`, color: "#ccffcc" }
      : { text: `${diff.toFixed(1)} Hrs Short`, color: "#ff6666" };
